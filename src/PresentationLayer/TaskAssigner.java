    public void showTableEnteries() {
        enableTextFields();
        enableButtons(true);
        String heading[] = {"EmployeeID", "Name", "Contact", "Email", "Gender", "No of Jobs Done", "Salary", "Ratings", "Experience"};
        String data[][] = new String[ebal.size()][heading.length];
        for (int i = 0; i < ebal.size(); i++) {
            EmployeeBean objbean = ebal.get(i);
            LoginBean loginbean = ManageUser.getSingleLoginBean(objbean.getLogin_id());
            data[i][0] = String.valueOf(objbean.getEmployee_id());
            data[i][1] = loginbean.getName();
            data[i][2] = String.valueOf(loginbean.getContact());
            data[i][3] = loginbean.getEmail();
            if (loginbean.isGender()) {
                data[i][4] = "Male";
            } else {
                data[i][4] = "Female";
            }
            data[i][5] = String.valueOf(objbean.getNo_of_jobs_done());
            data[i][6] = String.valueOf(objbean.getHourly_salary());
            data[i][7] = String.valueOf(objbean.getRatings());
            data[i][8] = String.valueOf(objbean.getExperience());

        }
        tableEmployeeList = new JTable(data, heading);
        jScrollPane1.setViewportView(tableEmployeeList);

    }

