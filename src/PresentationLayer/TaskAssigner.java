    private void entryIntoTable() {
        if (txtEmployeeId.getText().isEmpty()) {
            Utilities.showMsg(this, "Please Enter user Id", 2);
            txtEmployeeId.requestFocus();
        } else {
            double salary = 0.0;
            boolean flag = false;
            for (int i = 0; i < ebal.size(); i++) {
                if (ebal.get(i).getEmployee_id() == Integer.parseInt(txtEmployeeId.getText())) {
                    flag = true;
                    salary = ebal.get(i).getHourly_salary() * 12 * 30;
                }
            }
            if (flag) {
                TaskBean objbean = new TaskBean();
                objbean.setEmployee_id((Integer.parseInt(txtEmployeeId.getText())));
                objbean.setEmp_id(emp_id);
                objbean.setProject_id(project_id);
                objbean.setStatus(true);
                if (TaskManager.addTask(objbean)) {
                    Utilities.showMsg(this, "Task Assigned Succesfully", 0);
                    txtNoofEmployeesSelected.setText(String.valueOf(--no_of_employess_selected));
                    txtRemainingBudget.setText(String.valueOf(budget_left - salary));

                } else {
                    Utilities.showMsg(this, "Task Already Assigned", 1);
                }
            } else {
                Utilities.showMsg(this, "Enter Correct Employee Id", 1);
            }
        }
    }
