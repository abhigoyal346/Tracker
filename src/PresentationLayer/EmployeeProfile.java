    public void showRecord() {
       
        
        
        if (employeeobjbean == null) {
            Utilities.showMsg(this, "Please Update All Details", 1);
        } else {
            txtID.setText(String.valueOf(this.employee_id));
            txtRatings.setText(String.valueOf(employeeobjbean.getRatings()));
            txtExperience.setText(String.valueOf(employeeobjbean.getExperience()));
            txtNoOfJobsDone.setText(String.valueOf(employeeobjbean.getNo_of_jobs_done()));
            txtLocation.setText(employeeobjbean.getLocation());
            txtSalary.setText(String.valueOf((employeeobjbean.getHourly_salary())));
            txtDateofJoining.setText(employeeobjbean.getDate_of_joining());
        }
        btnSave.setEnabled(false);
        btnCancel.setEnabled(true);
        btnEdit.setEnabled(true);
        enableTextFields(false);
        txtName.setText(loginbean.getName());
        txtContactNo.setText(loginbean.getContact());
        if (loginbean.isGender()) {
            rbMale.setSelected(true);
        } else {
            rbFemale.setSelected(true);
        }
        txtDob.setText(loginbean.getDob());
        txtEmailId.setText(loginbean.getEmail());
        employee_password = loginbean.getPassword();
        setProfile();
    }

