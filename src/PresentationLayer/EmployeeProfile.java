    private void lbImg1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbImg1MouseClicked
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        //filter the files
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.Images", "jpg");
        file.addChoosableFileFilter(filter);
        int result = file.showSaveDialog(null);
        //if the user click on save in Jfilechooser
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = file.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            try {
                String profilePath = Utilities.clientProfilePhotoLocation() + "\\" + loginbean.getLogin_id() + ".jpg";if (new File(profilePath).exists()) {
                    boolean flag = new File(profilePath).delete();
                    if (flag) {
                        Files.move(Paths.get(path), Paths.get(profilePath));
                        setProfile();
                    }
                }
            } catch (Exception e) {
                System.out.println("File Move Exception in EmployeeProfile" + e);
            }
        }
    }//GEN-LAST:event_lbImg1MouseClicked
